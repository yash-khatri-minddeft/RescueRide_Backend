const mongoose = require('mongoose');

const ControllerSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  phoneNo: {
    type: Number,
    required: true,
  },
  gender: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true
  }
})

const controller = new mongoose.model('controller', ControllerSchema);

module.exports = controller;